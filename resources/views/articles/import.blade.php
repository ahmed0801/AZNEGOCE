document.getElementById('fileInput').addEventListener('change', function(e) {
    const file = e.target.files[0];
    if (!file) return;

    const preview = document.getElementById('preview');
    preview.innerHTML = `<p>Fichier sélectionné : <strong>${file.name}</strong> (${(file.size/1024).toFixed(2)} KB)</p>`;

    // Prévisualisation avec fetch
    const formData = new FormData();
    formData.append('file', file);
    formData.append('_token', '{{ csrf_token() }}');

    fetch('{{ route("articles.import.preview") }}', {
        method: 'POST',
        body: formData
    })
    .then(res => {
        if (!res.ok) throw new Error('Réponse réseau non OK');
        return res.json();
    })
    .then(data => {
        let html = '<table class="table table-bordered table-sm">';
        html += '<thead><tr>';
        for (const key of Object.keys(data.preview[0])) {
            html += `<th>${key}</th>`;
        }
        html += '</tr></thead><tbody>';
        for (const row of data.preview) {
            html += '<tr>';
            for (const value of Object.values(row)) {
                html += `<td>${value}</td>`;
            }
            html += '</tr>';
        }
        html += '</tbody></table>';

        if(data.errors && data.errors.length) {
            html += '<div class="alert alert-danger"><ul class="mb-0">';
            for(const err of data.errors) {
                html += `<li>${err}</li>`;
            }
            html += '</ul></div>';
        }

        preview.innerHTML += html;
    })
    .catch(err => {
        console.error('Erreur:', err);
        preview.innerHTML += '<div class="alert alert-danger">Erreur de prévisualisation. Vérifiez le format du fichier.</div>';
    });
});
</script>

